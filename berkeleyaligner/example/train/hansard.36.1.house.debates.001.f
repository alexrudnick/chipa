le Président : 
je tiens à commencer par répéter les premiers mots que , depuis de les centaines de années , tous les Présidents prononcent . 
honorables députés , je tiens à exprimer à la Chambre mes humbles remerciements pour le grand honneur que elle a bien voulu me faire en me choisissant comme Président . 
je tiens à vous remercier de la confiance que vous venez de me témoigner . 
je suis à la fois très reconnaissant et profondément touché . 
il se en trouve pour dire que il y aura beaucoup de action à la Chambre à le cours de cette 36e législature . 
je ne ai jamais connu de législature où il ne y avait pas beaucoup de action . 
pourquoi ne y aurait - il pas beaucoup de action ? 
vous êtes ici en tant que les porte - parole élus de les Canadiens de tout le pays . 
vous allez défendre comme il se doit de les opinions bien arrêtées . 
ce est ici , sur le parquet de la Chambre de les communes , que nous aurons la chance de nous exprimer . 
la dernière fois que vous me avez fait le honneur de me élire comme Président , je ai dit que nous étions les gladiateurs politiques de le Canada , et ce est vrai . 
nos armes sont nos paroles , nos idées , nos convictions . 
voilà pourquoi il y a de le action . 
mais nous pouvons faire notre travail dans un esprit de respect le un pour le autre et pour notre institution . 
mon travail est de veiller à ce que les droits et privilèges de tous les parlementaires canadiens soient protégés . 
de une manière générale , nous avons , vous et moi , trois droits et privilèges fondamentaux . 
nous avons le droit de venir à cet endroit . 
nous avons le droit de parler sans crainte , et celui de voter . 
je vous promets de exécuter mes fonctions dans un esprit de équité et de impartialité . 
je suis , comme vous le savez , à le service de la Chambre . 
ce est la Chambre qui , collectivement , décide si elle veut apporter de les changements et de quelle manière elle procédera à ces changements . 
parlementaires de le Canada , à le cours de les prochaines années , en raison de la situation , de le histoire et , oui , de le destin , le sort de notre pays sera entre nos mains . 
je vous remercie de nouveau pour le honneur que vous me avez fait aujourd' hui . 
et maintenant , le Parlement peut se mettre à le travail . 
je vous remercie beaucoup . 
