PREMIÈRE SESSION - 36E LÉGISLATURE 
la trente - cinquième législature ayant été prorogée et les Chambres dissoutes par proclamation le dimanche 27 avril 1997 , puis les brefs ayant été émis et rapportés , les nouvelles Chambres ont été convoquées pour le expédition de les affaires le lundi 22 septembre 1997 et , en conséquence , se sont réunies le jour dit . 
le lundi 22 septembre 1997 . 
le Parlement ayant été convoqué pour aujourd' hui , par proclamation de Son Excellence le Gouverneur général de le Canada pour le expédition de les affaires , et les députés étant réunis : 
m . Robert Marleau , greffier de la Chambre , donne lecture de une lettre de le directeur administratif de le Gouverneur général annonçant que le très honorable Antonio Lamer , à titre de suppléant de le Gouverneur général , se rendra à la salle de le Sénat le lundi 22 septembre 1997 , à Ottawa , pour ouvrir la première session de la trente - sixième législature . 
le gentilhomme huissier de la verge noire apporte le message suivant : 
membres de la Chambre de les communes : 
ce est le désir de le honorable suppléant de Son Excellence le Gouverneur général que cette honorable Chambre se rende immédiatement auprès de lui dans la salle de le honorable Sénat . 
honorables membres de le Sénat , membres de la Chambre de les communes : 
je suis chargé de vous informer que Son Excellence le Gouverneur général de le Canada ne juge pas à propos de faire connaître , avant que la Chambre de les communes ne ait choisi un Président conformément à la loi , les motifs qui le ont porté à convoquer le Parlement , mais que il les exposera demain le mardi 23 septembre 1997 , à 14 h 30 . 
et les députés étant revenus à la Chambre de les communes . 
le greffier de la Chambre : 
conformément à le article 3 de le Règlement , je inviteM. Caccia , député de la circonscription de Davenport , à venir prendre place à le fauteuil afin de présider à le élection de un Président . 
ÉLECTION DU PRÉSIDENT 
le président de élection (M. Caccia ) : 
chers collègues , vous me faites un grand honneur en me confiant cette tâche . 
en ce premier jour de automne , je aimerais souhaiter la plus cordiale bienvenue à tous les députés et surtout à les 90 nouveaux parlementaires siégeant en cette Chambre , laquelle compte maintenant 301 sièges . 
six nouveaux sièges ont été ajoutés à la Chambre de les communes afin que elle reflète adéquatement le nombre croissant de électeurs canadiens . 
la tâche qui nous attend aujourd' hui est simple et précise : élire un Président , conformément à les dispositions de les articles 2 à 6 de le chapitre 1 de le Règlement . 
je voudrais attirer le attention de les députés sur le fait que , en vertu de le Règlement , nous allons commencer avec la liste de les candidats et , tout de abord , celle de les députés qui ont retiré leur candidature ou qui ne sont pas éligibles . 
la liste de les députés éligibles à la Présidence a également été déposée sur le pupitre de chaque député . 
elle est disponible à le Bureau et elle est affichée dans chaque isoloir . 
lorsque le greffier aura ouvert les bulletins , la présidence proposera une méthode pour accélérer le processus de scrutin . 
comme nous allons maintenant commencer à voter , je rappelle à les honorables députés que ils doivent écrire le prénom et le nom de leur candidat en lettres moulées sur le bulletin de vote . 
je propose que les députés quittent leur fauteuil , sortent par derrière les rideaux et viennent à la table en utilisant les portes à droite et à gauche de le fauteuil présidentiel , selon le côté où ils siègent . 
le greffier donnera un bulletin à chaque député . 
après avoir voté , les députés voudront bien se éloigner de les isoloirs . 
les isoloirs sont maintenant ouverts pour le vote . 
les députés peuvent maintenant se avancer pour voter . 
( les députés reçoivent leur bulletin de vote , que ils remplissent en secret dans les isoloirs . ) 
le président de élection (M. Caccia ) : 
les députés qui ne ont pas voté et qui souhaitent le faire devraient voter maintenant . 
tous les députés ayant voté , je ordonne à le greffier de procéder à le dépouillement de le scrutin une fois que je aurai déposé mon bulletin de vote . 
suspension DE LA SÉANCE 
le président de élection (M. Caccia ) : 
avant de suspendre la séance , je signale à les députés que , lorsque on aura fini de dépouiller les bulletins de vote , on ne fera retentir la sonnerie rappelant les députés à la Chambre que pendant cinq minutes . 
la séance est suspendue jusque à le appel de le Timbre . 
( la séance est suspendue jusque à 12 h 10 . ) 
